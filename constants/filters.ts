export interface FilterPreset {
  id: string;
  name: string;
  nameKo: string;
  nameJa: string;
  nameZh: string;
  /** RGBA overlay color */
  overlay: string;
  /** 0-1 overlay opacity */
  overlayOpacity: number;
  /** brightness multiplier: 1 = normal */
  brightness: number;
  /** contrast multiplier: 1 = normal */
  contrast: number;
  /** saturation multiplier: 1 = normal, 0 = grayscale */
  saturation: number;
}

